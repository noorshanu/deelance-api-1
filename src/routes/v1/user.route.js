const express = require('express');
const userController = require('../../modules/users/controller');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate')
const userValidation = require('../../modules/users/user.validation')
const router = express.Router();


router.get('/all-users', validate(userValidation.listUsers), userController.getUsersList);
router.get('/userInfo/:id', userController.getUserDetailsById);
router.put('/block-user/:id', validate(userValidation.listUsers), userController.updateUserStatus);
router.post('/update', auth(), validate(userValidation.updateProfile), userController.updateUserProfile);
router.post('/wallet', auth(), userController.updateUserWallet);
router.get('/searchuser/:search', auth("manageUsers"), userController.searchUser)

module.exports = router;

